std::vector<std::string> stringToVector(std::string path) {
  std::vector<std::string> vector;

  std::string current_name("");

  for(char & c : path) {
    if(c == '/') {
      vector.push_back(current_name);
      current_name.clear();
    } else {
      current_name += c;
    }
  }

  return vector;
}

